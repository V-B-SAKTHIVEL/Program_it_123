class RelationalOperator
{
    public static void main(String args[])
    {
        int a = 5, b = 7;
        System.out.println("a = " + a + " b = " + b);
        System.out.println("\na =  b is " + (a==b));
        System.out.println("a != b is " + (a != b));
        System.out.println("a > b is " + (a > b));
        System.out.println("a < b is " + (a < b));
        System.out.println("a >= b is " + (a >= b));
        System.out.println("a <= b is " + (a <= b));
    }
}